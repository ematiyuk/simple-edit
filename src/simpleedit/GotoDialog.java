    private void jGoToButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jGoToButtonActionPerformed
        if (!jInputTextField.getText().isEmpty()) {
            if (Utils.validateForNumbersOnly(jInputTextField.getText())) {
                int lineNumb = Integer.decode(jInputTextField.getText());
                if (this.gotoLine(lineNumb)) {
                    this.hideDialog();
                } else {
                    String message = "Недопустимый ввод номера строки.";
                    JOptionPane.showMessageDialog(jGoToButton, message, this.getProgramName() + " - Переход на строку", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                String message = "Неверные входные данные.\nДопускаются исключительно цифровые значения.";
                JOptionPane.showMessageDialog(jGoToButton, message, this.getProgramName() + " - Переход на строку", JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            String message = "Нет входных данных.\nУкажите, пожалуйста, номер строки.";
            JOptionPane.showMessageDialog(jGoToButton, message, this.getProgramName() + " - Переход на строку", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jGoToButtonActionPerformed

