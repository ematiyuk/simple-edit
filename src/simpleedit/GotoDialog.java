    public boolean gotoLine(int lineNumber) {
        if ((lineNumber > 0) && (lineNumber <= Utils.getTextLinesCount(currTextArea.getText()))) {
            int line = 0;
            int firstLineLength = 0;
            int textLength = currTextArea.getText().length();

            for (int i = 0; i < textLength; i++) {
                if (currTextArea.getText().charAt(i) == '\n') {
                    firstLineLength = i;
                    break;
                }
            }

            for (int i = 0; i < textLength; i++) {
                if ((i >= 0) && (i < firstLineLength)) {
                    line = 1;
                } else {
                    if ((currTextArea.getText().charAt(i) == '\n') || (i == (textLength - 1))) {
                        line++;
                    }
                }
                if (line == lineNumber) {
                    if (line == 1) {
                        currTextArea.setCaretPosition(0);
                    } else {
                        currTextArea.setCaretPosition(i + 1);
                    }
                    return true;
                }
            }
            return true;
        } else {
            return false;
        }
    }

